/**
 * Represents a 6D vector consisting of three force components and three torque components.
 */
export interface Vector6D {
    /**
     * U-axis
     */
    u: number;
    /**
     * V-axis
     */
    v: number;
    /**
     * W-axis
     */
    w: number;
    /**
     * X-axis
     */
    x: number;
    /**
     * Y-axis
     */
    y: number;
    /**
     * Z-axis
     */
    z: number;
}
